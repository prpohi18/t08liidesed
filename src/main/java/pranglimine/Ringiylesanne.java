package pranglimine;
public class Ringiylesanne{
    double raadius;
    double ringiPindala;
    public Ringiylesanne(double arv1, double arv2){
        raadius=arv1*arv1;
        arv2=Math.PI;
        ringiPindala=raadius*arv2;
    }
    @Override
    public String toString(){
        return raadius+"*"+Math.PI+"=";
    }
    public double kysiRingiPindala(){
        return ringiPindala;
    }
    public double kysiRaadius(){
        return raadius;
    }
}